package domain;
public class Person {
	
	private String _firstName;
	public String getFirstName() {
		return _firstName;
	}
	public void setName(String firstName) {
		_firstName = firstName;
	}
	
	private String _eMail;
	public String getEMail() {
		return _eMail;
	}
	public void setEMail(String eMail) {
		_eMail = eMail;
	}
	
}
